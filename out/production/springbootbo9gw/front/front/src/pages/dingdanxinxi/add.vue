            wupinmingcheng : false,
            wuliuleixing : false,
            zhongliang : false,
            feiyong : false,
            shoujianren : false,
            lianxidianhua : false,
            shoujiandizhi : false,
            xiadanshijian : false,
            yonghuming : false,
            xingming : false,
            shouji : false,
            zhuangtai : false,
            ispay : false,
        },
        type: '',
        userTableName: localStorage.getItem('UserTableName'),
